// Reference date: BS 2000/1/1 = AD 1943/4/14
const BS_START_YEAR = 2000;
const AD_REF_DATE = new Date(1943, 3, 14); // April 14, 1943

const BS_MONTH_NAMES = [
    'बैशाख', 'जेठ', 'असार', 'श्रावण', 'भदौ', 'असोज',
    'कार्तिक', 'मंसिर', 'पुष', 'माघ', 'फाल्गुन', 'चैत्र'
];

const BS_MONTH_NAMES_EN = [
    'Baisakh', 'Jestha', 'Asar', 'Shrawan', 'Bhadra', 'Asoj',
    'Kartik', 'Mangsir', 'Poush', 'Magh', 'Falgun', 'Chaitra'
];

const BS_DAY_NAMES = ['आइत', 'सोम', 'मंगल', 'बुध', 'बिहि', 'शुक्र', 'शनि'];

export interface BSDate {
    year: number;
    month: number;
    day: number;
}

/**
 * Get total days in a BS year
 */
function getTotalDaysInBSYear(year: number): number {
    const yearIndex = year - BS_START_YEAR;
    if (yearIndex < 0 || yearIndex >= BS_MONTHS.length) return 365;
    return BS_MONTHS[yearIndex]!.reduce((sum, days) => sum + days, 0);
}

/**
 * Get days in a specific BS month
 */
function getDaysInBSMonth(year: number, month: number): number {
    const yearIndex = year - BS_START_YEAR;
    if (yearIndex < 0 || yearIndex >= BS_MONTHS.length) return 30;
    return BS_MONTHS[yearIndex]![month - 1] || 30;
}

/**
 * Convert AD date to BS date
 */
export function adToBS(adDate: Date): BSDate {
    // Calculate total days from reference date
    const diffTime = adDate.getTime() - AD_REF_DATE.getTime();
    let totalDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));

    // Start from BS_START_YEAR/1/1
    let bsYear = BS_START_YEAR;
    let bsMonth = 1;
    let bsDay = 1;

    // Add days year by year
    while (totalDays > 0) {
        const yearDays = getTotalDaysInBSYear(bsYear);
        if (totalDays >= yearDays) {
            totalDays -= yearDays;
            bsYear++;
        } else {
            break;
        }
    }

    // Add days month by month
    while (totalDays > 0) {
        const monthDays = getDaysInBSMonth(bsYear, bsMonth);
        if (totalDays >= monthDays) {
            totalDays -= monthDays;
            bsMonth++;
            if (bsMonth > 12) {
                bsMonth = 1;
                bsYear++;
            }
        } else {
            break;
        }
    }

    bsDay += totalDays;

    return { year: bsYear, month: bsMonth, day: bsDay };
}

/**
 * Format BS date in Nepali
 */
export function formatBSDate(bsDate: BSDate, format: 'short' | 'long' = 'short'): string {
    const monthName = BS_MONTH_NAMES[bsDate.month - 1] || '';
    if (format === 'long') {
        return `${bsDate.year} ${monthName} ${bsDate.day}`;
    }
    return `${bsDate.year}/${bsDate.month}/${bsDate.day}`;
}

/**
 * Format BS date in English
 */
export function formatBSDateEN(bsDate: BSDate, format: 'short' | 'long' = 'short'): string {
    const monthName = BS_MONTH_NAMES_EN[bsDate.month - 1] || '';
    if (format === 'long') {
        return `${bsDate.day} ${monthName}, ${bsDate.year}`;
    }
    return `${bsDate.year}/${bsDate.month}/${bsDate.day}`;
}

/**
 * Get Nepali day name
 */
export function getBSDayName(adDate: Date): string {
    return BS_DAY_NAMES[adDate.getDay()] || '';
}

/**
 * Get current BS date
 */
export function getCurrentBSDate(): BSDate {
    return adToBS(new Date());
}
